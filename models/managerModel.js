const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const managerSchema = new Schema({
  username: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
});

const Manager = mongoose.model("Manager", managerSchema);
module.exports = Manager;
